export function byteToBinary8(n: number): string {
  return (n & 0xff).toString(2).padStart(8, '0');
}

export function byteToHex(n: number): string {
  return '0x' + (n & 0xff).toString(16).padStart(2, '0');
}

/** Parse input as one byte (0–255). Uses last 8 bits if longer. */
export function parseByteBits(bits: string): number | null {
  const c = bits.replace(/\s/g, '');
  if (!/^[01]+$/.test(c)) return null;
  const byteBits = c.slice(-8).padStart(8, '0');
  return parseInt(byteBits, 2);
}

export function lookupSBox(
  table: readonly number[],
  index: number,
): { index: number; value: number; hex: string; result: string } {
  const value = table[index]! & 0xff;
  return {
    index,
    value,
    hex: byteToHex(value),
    result: byteToBinary8(value),
  };
}
